/*
    Comparation Operator
    
    Vamos agora falar sobre a comparação estrita!
    Esse método alem de ver se os elementos são iguais, confirma tambem o tipo dos elementos.
*/

let one = 1
let two = 2

// === (Estritamente igual a)

console.log(one === "1")
console.log(one === 1)

// !== (Estritamende difenrente de )

console.log(two !== "2")
console.log(two !== 2)

