/*
    Comparation Operator
    
    Continuando no tema vamos para outro tipo de comparação, maior que ou menor que e outros!
*/

let one = 1
let two = 2

//  > (Maior que!)

console.log(one > two)

// >= (Maior igaul a)

console.log(one >= 1)
console.log(two >= 1)

// < (Menor que!)

console.log(one < two)

// <= (Menor igual a)

console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)