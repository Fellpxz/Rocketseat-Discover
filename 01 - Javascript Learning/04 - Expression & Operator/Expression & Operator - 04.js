/*
    Operadores Aritiméticos

    Multiplicação 
    Divisão
    Soma 
    Subtração

    Resto da divisão
    Incremento
    Decremento
    Exponecial
*/

//  Mutiplicação
console.log(2.3 * 5.5)

//  Divisão
console.log(12 / 4)

//  Soma
console.log(33 + 12)

//  Subtração
console.log(22 - 10)

//  Resto da divisão %
let remainder
remainder = 11 % 9
console.log(remainder)

//  Incremento ++
let increment = 0
increment++
console.log(++increment)

//  Decremento --
let decrement = 0
decrement--
console.log(--decrement)

// Exponecial **
console.log(3 ** 3)